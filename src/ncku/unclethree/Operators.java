package ncku.unclethree;

public class Operators {
    public static void main(String[] args){
        float valueintOne = 404f;
        float valueintTwo = 200f;

        System.out.println("ValueOne + ValueTwo = " + (valueintOne + valueintTwo));
        System.out.println("ValueOne - ValueTwo = " + (valueintOne - valueintTwo));
        System.out.println("ValueOne * ValueTwo = " + (valueintOne * valueintTwo));
        System.out.println("ValueOne / ValueTwo = " + (valueintOne / valueintTwo));
        System.out.println("ValueOne % ValueTwo = " + (valueintOne % valueintTwo));
    }
}

